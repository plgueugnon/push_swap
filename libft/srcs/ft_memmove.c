#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	size_t	i;

	i = 0;
	if ((char *)dst == NULL && (const char *)src == NULL)
		return (NULL);
	if ((char *)dst > (const char *)src)
	{
		while (i < len)
		{
			((char *)dst)[len - 1] = ((char *)src)[len - 1];
			len--;
		}
	}
	else
	{
		while (i < len)
		{
			*((char *)dst) = *((const char *)src);
			dst++;
			src++;
			i++;
		}
	}
	return (dst);
}
