long	ft_atol(const char *str)
{
	long	s;
	long	r;

	s = 1;
	r = 0;
	while (*str && ((*str >= 9 && *str <= 13) || *str == 32))
		str++;
	if (*str == '-')
	{
		s *= -1;
		str++;
	}
	else if (*str == '+')
		str++;
	while (*str && (*str >= 48 && *str <= 57))
	{
		r = r * 10 + *str - 48;
		str++;
	}
	return (r * s);
}
